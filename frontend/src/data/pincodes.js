// Get valid pincodes for a specific city and state
export const getPincodesForCity = (city, state) => {
  return pincodeData[state]?.[city] || [];
};

// Check if a pincode is valid for a specific city and state
export const isValidPincode = (pincode, city, state) => {
  if (!pincode || !city || !state) return false;
  const validPincodes = getPincodesForCity(city, state);
  return validPincodes.includes(pincode);
};

// Get all pincodes for a state (across all cities)
export const getPincodesForState = (state) => {
  if (!pincodeData[state]) return [];
  const allPincodes = [];
  Object.values(pincodeData[state]).forEach(cityPincodes => {
    allPincodes.push(...cityPincodes);
  });
  return [...new Set(allPincodes)]; // Remove duplicates
};

// Find city and state for a given pincode
export const findLocationByPincode = (pincode) => {
  for (const [state, cities] of Object.entries(pincodeData)) {
    for (const [city, pincodes] of Object.entries(cities)) {
      if (pincodes.includes(pincode)) {
        return { city, state };
      }
    }
  }
  return null;
};